const API_KEY = process.env.GEOCODING_API_KEY

export default async function reverseLookup(reverse: {lon: string, lat: string}) {
    const u = new URL('http://api.openweathermap.org/geo/1.0/reverse')
    u.searchParams.set('lat', reverse.lat)
    u.searchParams.set('lon', reverse.lon)
    u.searchParams.set('limit', "1")
    u.searchParams.set('appid', API_KEY)

    const [resp] = await (await fetch(u.toString())).json()
    return {city: resp.name, state: stateMap[resp.state] }
}


